    public void mostrarJTableEstudiantes(){
        try {
            
            DefaultTableModel mod = new DefaultTableModel();
            jTableEstudiantes.setModel(mod);
            PreparedStatement pst = c.conexion.prepareStatement("Select * from Estudiantes");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            int cantColums = rsmd.getColumnCount();
            
            mod.addColumn("Nombres");
            mod.addColumn("Apellidos");
            mod.addColumn("Fecha de nacimiento");
            mod.addColumn("Correo Institucional");
            mod.addColumn("Correo Personal");
            mod.addColumn("Número Celular");
            mod.addColumn("Número Fijo");
            mod.addColumn("Programa");
            
            while(rs.next()){
                Object[] filas = new Object[cantColums];
                for (int i = 0; i < cantColums; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                mod.addRow(filas);
            }
            pst.close();
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
    
    
    
    public void buscarEstudiantes(String correo_ins){
        try {
            PreparedStatement pst = c.conexion.prepareStatement("Select * from Estudiantes where Correo_Institucional = ?");
            pst.setString(1, correo_ins);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                txtNombres.setText(rs.getString("Nombres"));
                txtApellidos.setText(rs.getString("Apellidos"));
                txtFecha.setText(rs.getString("Fecha_Nacimiento"));
                txtCorreo_ins.setText(rs.getString("Correo_Institucional"));
                txtCorreo_per.setText(rs.getString("Correo_Personal"));
                txtNum_cel.setText(rs.getString("Numero_Celular"));
                txtNum_fijo.setText(rs.getString("Numero_fijo"));
                txtPrograma.setText(rs.getString("Programa"));
            } else {
                JOptionPane.showMessageDialog(null, "No se encontro ningun registro", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
            }
            pst.close();
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
    
    
    
