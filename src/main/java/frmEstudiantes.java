    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!txtNombres.getText().isEmpty() && !txtApellidos.getText().isEmpty() && !txtFecha.getText().isEmpty() && !txtCorreo_ins.getText().isEmpty() && !txtCorreo_per.getText().isEmpty() && !txtNum_cel.getText().isEmpty() && !txtNum_fijo.getText().isEmpty() && !txtPrograma.getText().isEmpty()){
            correo_ins = txtCorreo_ins.getText();
            if (!e.verificarBD(correo_ins)) {
                nombres = txtNombres.getText();
                apellidos = txtApellidos.getText();
                fecha = txtFecha.getText();
                correo_ins = txtCorreo_ins.getText();
                correo_per = txtCorreo_per.getText();
                num_cel = Long.parseLong(txtNum_cel.getText());
                num_fijo = Long.parseLong(txtNum_fijo.getText());
                programa = txtPrograma.getText();
                e.insertarEstudiante(nombres, apellidos, fecha, correo_ins, correo_per, num_cel, num_fijo, programa);
                JOptionPane.showMessageDialog(null,"El registro fue grabado exitosamente", "Registro de estudiantes", JOptionPane.INFORMATION_MESSAGE);
                mostrarJTableEstudiantes();
                limpiarDatos();
            } else {
                JOptionPane.showMessageDialog(null, "El correo " + correo_ins + " ya existe", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
                txtCorreo_ins.requestFocus();
            }            
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos por llenar", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (!txtCorreo_ins.getText().isEmpty() && !txtCorreo_per.getText().isEmpty() && !txtNum_cel.getText().isEmpty() && !txtNum_fijo.getText().isEmpty() && !txtPrograma.getText().isEmpty()){
            correo_ins = txtCorreo_ins.getText();
            if (e.verificarBD(correo_ins)) {
                correo_ins = txtCorreo_ins.getText();
                correo_per = txtCorreo_per.getText();
                num_cel = Long.parseLong(txtNum_cel.getText());
                num_fijo = Long.parseLong(txtNum_fijo.getText());
                programa = txtPrograma.getText();
                e.modificarEstudiante(correo_ins, correo_per, num_cel, num_fijo, programa);
                JOptionPane.showMessageDialog(null,"El registro fue actualizado exitosamente", "Registro de estudiantes", JOptionPane.INFORMATION_MESSAGE);
                mostrarJTableEstudiantes();
                limpiarDatos();
            } else {
                JOptionPane.showMessageDialog(null, "El correo " + correo_ins + " no existe", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
                txtCorreo_ins.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos por llenar", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtCorreo_ins.getText().isEmpty()){
            correo_ins = txtCorreo_ins.getText();
            if (e.verificarBD(correo_ins)) {
                e.eliminarEstudiante(correo_ins);
                JOptionPane.showMessageDialog(null,"El registro fue eliminado exitosamente", "Registro de estudiantes", JOptionPane.INFORMATION_MESSAGE);
                mostrarJTableEstudiantes();                
                limpiarDatos();
            } else {
                JOptionPane.showMessageDialog(null, "El correo " + correo_ins + " no existe", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
                txtCorreo_ins.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos por llenar", "Registro de estudiantes", JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_btnEliminarActionPerformed

